var marks = 77;

if (marks >= 90) {
    console.log('got A+');
}
else if (marks >= 80 && marks <= 90) {
    console.log('got A');
}
else if (marks >= 70 && marks <= 80) {
    console.log('got B');
}
else if (marks >= 60 && marks <= 70) {
    console.log('got C');
}

else if (marks >= 50 && marks <= 60) {
    console.log('got D');
}
else {
    console.log('got failed');
}